// Utility functions

function formatCurrency(value, compact = false) {
  if (compact && Math.abs(value) >= 1e9) return '$' + (value / 1e9).toFixed(2) + 'B';
  if (compact && Math.abs(value) >= 1e6) return '$' + (value / 1e6).toFixed(2) + 'M';
  return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', minimumFractionDigits: 2 }).format(value);
}

function formatPercent(value) {
  const sign = value >= 0 ? '+' : '';
  return sign + value.toFixed(2) + '%';
}

function formatNumber(value) {
  return new Intl.NumberFormat('en-US').format(value);
}

function formatLargeNumber(value) {
  if (Math.abs(value) >= 1e12) return (value / 1e12).toFixed(2) + 'T';
  if (Math.abs(value) >= 1e9)  return (value / 1e9).toFixed(2) + 'B';
  if (Math.abs(value) >= 1e6)  return (value / 1e6).toFixed(2) + 'M';
  if (Math.abs(value) >= 1e3)  return (value / 1e3).toFixed(2) + 'K';
  return value.toString();
}

function getChangeColor(value) {
  return value >= 0 ? 'text-positive' : 'text-negative';
}

function getChangeBg(value) {
  return value >= 0
    ? 'background:rgba(16,185,129,0.15);color:#10b981;'
    : 'background:rgba(239,68,68,0.15);color:#ef4444;';
}

function timeAgo(isoString) {
  const diff = (Date.now() - new Date(isoString).getTime()) / 1000;
  if (diff < 60)   return Math.floor(diff) + 's ago';
  if (diff < 3600) return Math.floor(diff / 60) + 'm ago';
  if (diff < 86400) return Math.floor(diff / 3600) + 'h ago';
  return Math.floor(diff / 86400) + 'd ago';
}

function escapeHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function formatAIResponse(text) {
  return escapeHtml(text)
    .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    .replace(/\n\n/g, '</p><p>')
    .replace(/\n/g, '<br>');
}

// Generate sparkline-like random price history
function generatePriceHistory(basePrice, days = 30) {
  const data = [];
  let price = basePrice * 0.85;
  for (let i = 0; i < days; i++) {
    price = price * (1 + (Math.random() - 0.48) * 0.03);
    data.push(parseFloat(price.toFixed(2)));
  }
  data.push(basePrice); // end at current price
  return data;
}

